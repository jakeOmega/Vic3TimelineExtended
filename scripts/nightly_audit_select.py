def load_state() -> dict:
    if not STATE_FILE.exists():
        return {"version": 1, "files": {}}
    try:
        with open(STATE_FILE, "r", encoding="utf-8") as f:
            data = json.load(f)
    except (OSError, json.JSONDecodeError):
        return {"version": 1, "files": {}}
    data.setdefault("version", 1)
    data.setdefault("files", {})
    return data


# --------------------------------------------------------------------------- #
# Scoring & selection
# --------------------------------------------------------------------------- #

def seeded_rng(date_str: str) -> random.Random:
    seed = int(hashlib.sha256(date_str.encode("utf-8")).hexdigest()[:8], 16)
    return random.Random(seed)


def days_since(iso_date: str | None, today: Date) -> int:
    if not iso_date:
        return LARGE_NUM
    try:
        last = datetime.strptime(iso_date, "%Y-%m-%d").date()
    except ValueError:
        return LARGE_NUM
    return max(0, (today - last).days)


def score_candidate(rel: str, state: dict, today: Date, rng: random.Random) -> float:
    file_state = state.get("files", {}).get(rel, {})
    days = days_since(file_state.get("last_audited"), today)
    findings = file_state.get("recent_findings", 0) or 0
    recency_boost = min(findings, RECENT_FINDINGS_CAP) * 0.5
    jitter = rng.uniform(0.0, 1.0)
    return days + recency_boost + jitter


def pick_slice(line_count: int, partial_coverage: list[list[int]]) -> tuple[int, int]:
    """Pick a contiguous slice of length min(line_count, SLICE_CAP) with least
    overlap to any prior coverage range. partial_coverage is [[start, end], ...]."""
    if line_count <= SLICE_CAP:
        return (1, line_count)
    step = max(SLICE_CAP // 4, 50)
    best_range = (1, SLICE_CAP)
    best_overlap = None
    for start in range(1, line_count - SLICE_CAP + 2, step):
        end = start + SLICE_CAP - 1
        overlap = 0
        for prior in partial_coverage:
            ps, pe = prior[0], prior[1]
            overlap += max(0, min(end, pe) - max(start, ps) + 1)
        if best_overlap is None or overlap < best_overlap:
            best_overlap = overlap
            best_range = (start, end)
    # Also consider the very last possible slice (covers the tail when step misses it)
    tail_start = line_count - SLICE_CAP + 1
    tail_end = line_count
    tail_overlap = sum(
        max(0, min(tail_end, p[1]) - max(tail_start, p[0]) + 1)
        for p in partial_coverage
    )
    if best_overlap is None or tail_overlap < best_overlap:
        best_range = (tail_start, tail_end)
    return best_range


def area_and_docs(rel: str) -> tuple[str, list[str]]:
    """Determine the area (checklist name) and the doc-reading list for a file."""
    docs: list[str] = []
    area: str | None = None
    for pattern, mapped_docs, mapped_area in DOC_MAPPING:
        if pattern.search(rel):
            if area is None:
                area = mapped_area
            for d in mapped_docs:
                if d not in docs:
                    docs.append(d)
    if area is None:
        area = "general"
    return area, docs


def select_targets(candidates: list[tuple[Path, str, int]], state: dict,
                   today: Date, rng: random.Random,
                   line_budget: int = LINE_BUDGET) -> list[dict]:
    """Iterative greedy pick under `line_budget` / FILE_CAP, with per-file
    slicing and a marginal-new-doc penalty.

    Seed pick is by base score alone. For each subsequent pick the effective
    score is `base - NEW_DOC_PENALTY * |candidate.docs - selected_docs|`,
    biasing toward files that don't expand the auditor's reading list.
    """
    pool = []
    for abs_path, rel, line_count in candidates:
        base = score_candidate(rel, state, today, rng)
        _, docs = area_and_docs(rel)
        pool.append({
            "base": base,
            "abs_path": abs_path,
            "rel": rel,
            "line_count": line_count,
            "docs": docs,
        })

    targets: list[dict] = []
    total_lines = 0
    selected_docs: set[str] = set()

    while pool and len(targets) < FILE_CAP:
        if not targets:
            # Seed: pure base-score order.
            pool.sort(key=lambda c: c["base"], reverse=True)
        else:
            pool.sort(
                key=lambda c: c["base"]
                - NEW_DOC_PENALTY * len(set(c["docs"]) - selected_docs),
                reverse=True,
            )

        picked_idx: int | None = None
        for i, cand in enumerate(pool):
            file_state = state.get("files", {}).get(cand["rel"], {})
            partial = file_state.get("partial_coverage", []) or []
            if cand["line_count"] > SLICE_CAP:
                line_range = list(pick_slice(cand["line_count"], partial))
                slice_lines = line_range[1] - line_range[0] + 1
            else:
                line_range = None
                slice_lines = cand["line_count"]

            if total_lines + slice_lines > line_budget and targets:
                # Doesn't fit — try the next candidate (some may be smaller).
                continue

            # Either fits, or this is the seed and exceeds budget alone — take it.
            area, docs = area_and_docs(cand["rel"])
            effective = cand["base"] - NEW_DOC_PENALTY * len(
                set(docs) - selected_docs
            ) if targets else cand["base"]
            targets.append({
                "path": cand["rel"],
                "line_range": line_range,
                "lines": slice_lines,
                "area": area,
                "docs": docs,
                "score": round(effective, 3),
                "last_audited": file_state.get("last_audited"),
                "audit_count": file_state.get("audit_count", 0),
                "recent_findings": file_state.get("recent_findings", 0),
            })
            total_lines += slice_lines
            selected_docs.update(docs)
            picked_idx = i
            break

        if picked_idx is None:
            # No remaining candidate fits the budget.
            break
        pool.pop(picked_idx)

    return targets


