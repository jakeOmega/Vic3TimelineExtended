def is_binary_file(path: Path) -> bool:
    """Heuristic: a file is binary if its first 1KB contains a NUL byte.

    Fast and reliable for distinguishing text from .dds/.png/.tga/etc. UTF-8
    text files with BOMs or non-ASCII chars pass this check correctly.
    """
    try:
        with open(path, "rb") as f:
            chunk = f.read(1024)
    except OSError:
        return True
    return b"\x00" in chunk


def has_autogen_marker(path: Path) -> bool:
    """Check the first 10 lines for AUTO-GENERATED / do not edit manually markers."""
    try:
        with open(path, "r", encoding="utf-8", errors="replace") as f:
            for _ in range(10):
                line = f.readline()
                if not line:
                    break
                if "AUTO-GENERATED" in line or "do not edit manually" in line:
                    return True
    except OSError:
        return False
    return False


def matches_any_glob(rel_path: str, globs: Iterable[str]) -> bool:
    """fnmatch each glob against the relative path."""
    for g in globs:
        if fnmatch.fnmatch(rel_path, g):
            return True
        # Also match if the glob is a directory prefix
        if g.endswith("/*") and rel_path.startswith(g[:-2] + "/"):
            return True
    return False


def enumerate_candidates() -> list[tuple[Path, str, int]]:
    """Walk SCAN_ROOTS and return (abs_path, rel_path, line_count) for each
    candidate. Excludes binary files, autogen-marked files, and EXCLUDED_GLOBS."""
    candidates: list[tuple[Path, str, int]] = []
    for root_rel in SCAN_ROOTS:
        root = REPO_ROOT / root_rel
        if not root.exists():
            continue
        for path in root.rglob("*"):
            if not path.is_file():
                continue
            rel = path.relative_to(REPO_ROOT).as_posix()
            if matches_any_glob(rel, EXCLUDED_GLOBS):
                continue
            # Schema/template files vanilla ships alongside data
            # (_institutions.info, political_movements.md, etc.) — docs,
            # not data; the audit can't act on them.
            if rel.startswith("common/") and path.suffix in (".md", ".info"):
                continue
            if is_binary_file(path):
                continue
            if has_autogen_marker(path):
                continue
            try:
                with open(path, "r", encoding="utf-8", errors="replace") as f:
                    line_count = sum(1 for _ in f)
            except OSError:
                continue
            if line_count == 0:
                continue
            candidates.append((path, rel, line_count))
    return candidates


def apply_filters(
    candidates: list[tuple[Path, str, int]],
    areas: set[str] | None,
    includes: list[str] | None,
    excludes: list[str] | None,
) -> list[tuple[Path, str, int]]:
    """Restrict candidates by area name and/or path globs.

    `areas` is an intersection: a candidate must resolve (via area_and_docs) to
    one of the named areas. `includes` is a disjunction: at least one glob must
    match the repo-relative path. `excludes` is a disjunction applied last:
    any match drops the candidate.
    """
    out: list[tuple[Path, str, int]] = []
    for abs_path, rel, line_count in candidates:
        if areas:
            area, _ = area_and_docs(rel)
            if area not in areas:
                continue
        if includes and not any(fnmatch.fnmatch(rel, g) for g in includes):
            continue
        if excludes and any(fnmatch.fnmatch(rel, g) for g in excludes):
            continue
        out.append((abs_path, rel, line_count))
    return out


