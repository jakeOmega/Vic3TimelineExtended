_PATH_LIKE_RE = re.compile(r"`([^`]+)`")
_EXT_RE = re.compile(r"\.[a-z]{2,5}$")
_BINARY_EXT_RE = re.compile(r"\.(dds|png|tga|jpg|jpeg|webp|gif|wav|ogg|mp3|asset|mesh)\b")


def _is_audit_relevant(entry: str) -> bool:
    """Drift only matters for paths/globs that would be audit candidates: under
    SCAN_ROOTS, and not obviously binary."""
    if not any(entry == root or entry.startswith(root + "/") for root in SCAN_ROOTS):
        return False
    if _BINARY_EXT_RE.search(entry):
        return False
    # `gfx/.../icons/*` patterns without extension are DDS icon dirs
    if "/icons/" in entry and entry.endswith("*"):
        return False
    return True


def parse_autogen_registry() -> set[str]:
    """Extract path-like entries from docs/auto_generated_files.md backtick-quoted cells.

    Strips triple-backtick code blocks first — otherwise the inline-backtick regex
    matches across them and swallows real paths as junk.
    """
    if not AUTOGEN_REGISTRY.exists():
        return set()
    text = AUTOGEN_REGISTRY.read_text(encoding="utf-8", errors="replace")
    text = re.sub(r"```.*?```", "", text, flags=re.DOTALL)
    paths: set[str] = set()
    for match in _PATH_LIKE_RE.finditer(text):
        candidate = match.group(1).strip()
        if "/" not in candidate:
            continue
        if not (_EXT_RE.search(candidate) or "*" in candidate):
            continue
        if candidate.startswith(("http://", "https://", "~/", "./")):
            continue
        if any(c in candidate for c in (" ", "(", ")", "$")):
            continue
        # Out of audit scope entirely
        if candidate.startswith("docs/data/"):
            continue
        paths.add(candidate)
    return paths


def detect_registry_drift() -> tuple[list[str], list[str]]:
    """Returns (registry_not_excluded, excluded_globs_not_in_registry).

    Only considers paths under SCAN_ROOTS and non-binary extensions — drift on
    docs/, scripts/, gfx icons, or scaffolding (always-excluded by policy) is
    not actionable for the audit.
    """
    registry = {e for e in parse_autogen_registry() if _is_audit_relevant(e)}
    excluded = set(EXCLUDED_REGISTRY_GLOBS)
    known_partial = set(INTENTIONALLY_NOT_EXCLUDED.keys())

    not_excluded: list[str] = []
    for entry in sorted(registry):
        if entry in known_partial:
            continue
        if any(fnmatch.fnmatch(entry, g) for g in excluded):
            continue
        if any(fnmatch.fnmatch(entry, g) for g in known_partial):
            continue
        not_excluded.append(entry)

    orphaned: list[str] = []
    for glob in sorted(excluded):
        if any(fnmatch.fnmatch(reg, glob) for reg in registry):
            continue
        if glob in registry:
            continue
        orphaned.append(glob)

    return not_excluded, orphaned


# --------------------------------------------------------------------------- #
# Output directory resolution (manual re-run versioning)
# --------------------------------------------------------------------------- #

_SLUG_RE = re.compile(r"[^a-zA-Z0-9]+")


def _slug_token(s: str, max_len: int = 20) -> str:
    """Normalize a glob/path fragment into a directory-safe slug."""
    cleaned = _SLUG_RE.sub("-", s).strip("-").lower()
    return cleaned[:max_len].rstrip("-")


def derive_filter_slug(areas: set[str] | None, includes: list[str] | None,
                       excludes: list[str] | None) -> str | None:
    """Build a short, directory-safe label from the active filters.

    Returns None if no filter is active. Slug format: `<areas>[-<include>][-not-<exclude>]`,
    capped at ~40 chars total.
    """
    if not (areas or includes or excludes):
        return None
    parts: list[str] = []
    if areas:
        if len(areas) >= 4:
            parts.append("multi")
        else:
            parts.append("-".join(sorted(areas)))
    if includes:
        tok = _slug_token(includes[0])
        if tok:
            parts.append(tok)
    if excludes:
        tok = _slug_token(excludes[0])
        if tok:
            parts.append(f"not-{tok}")
    slug = "-".join(p for p in parts if p)
    return slug[:40].rstrip("-") or None


def resolve_output_dir(base: Path, date_str: str, allow_rerun: bool,
                       filter_slug: str | None = None) -> tuple[Path, str]:
    """Pick the output directory and matching run label.

    Default (`allow_rerun=False`, no filter): always `<base>/<date_str>`, even
    if it already exists — the wrapper-driven scheduled path overwrites.

    Filtered runs (`filter_slug` set): base label is `<date_str>-<filter_slug>`
    instead of `<date_str>`. Re-run versioning still applies on top.

    Manual skill re-runs pass `allow_rerun=True`: if the base label dir already
    exists, return `<base label>-v2`, then `-v3`, etc., so prior runs'
    `prompt.md` / `targets.json` aren't clobbered. The run label is also returned
    so branch names and PR-body dir links in the rendered prompt point at the
    actual on-disk path.
    """
    base_label = f"{date_str}-{filter_slug}" if filter_slug else date_str
    if not allow_rerun:
        return base / base_label, base_label
    if not (base / base_label).exists():
        return base / base_label, base_label
    n = 2
    while (base / f"{base_label}-v{n}").exists():
        n += 1
    label = f"{base_label}-v{n}"
    return base / label, label


