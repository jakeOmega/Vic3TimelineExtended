class CatalogTests(unittest.TestCase):
    def test_load_engine_catalog_merges_with_builtins(self):
        # The mod's docs/engine/event_targets_summary.txt is generated by
        # the live engine; we point the loader at it.
        catalog = _load_engine_catalog("docs/engine")
        # Built-in country accessors should be present
        self.assertIn("country", catalog)
        self.assertIn("GetName", catalog["country"])
        # Some engine-derived accessors should also be present
        self.assertIn("active_law", catalog.get("country", {}))

    def test_load_engine_catalog_missing_dir_returns_builtins_only(self):
        catalog = _load_engine_catalog("/nonexistent/path/12345")
        self.assertIn("country", catalog)  # builtins still loaded
        self.assertIn("GetName", catalog["country"])

    def test_load_engine_catalog_handles_multi_scope_lines(self):
        """Lines like `country,front,state_region|every_scope_state|...`
        should spread the accessor across each named scope type."""
        with tempfile.TemporaryDirectory() as td:
            path = os.path.join(td, "event_targets_summary.txt")
            with open(path, "w", encoding="utf-8") as f:
                f.write(
                    "# header\n"
                    "country,front|every_scope_state|country|something\n"
                )
            catalog = _load_engine_catalog(td)
            self.assertEqual(catalog["country"]["every_scope_state"], "country")
            self.assertEqual(catalog["front"]["every_scope_state"], "country")


class ChainExtractionTests(unittest.TestCase):
    def test_extract_simple_chain(self):
        chains = extract_chains("Hello [TARGET_COUNTRY.GetName] world")
        self.assertEqual(len(chains), 1)
        self.assertEqual(chains[0].steps, ["TARGET_COUNTRY", "GetName"])

    def test_extract_multiple_chains(self):
        text = "[COUNTRY.GetName] vs [TARGET_COUNTRY.GetName]"
        chains = extract_chains(text)
        self.assertEqual(len(chains), 2)

    def test_extract_strips_format_modifier(self):
        chains = extract_chains("[JournalEntry.GetGoalProgressValue|D]")
        self.assertEqual(chains[0].steps, ["JournalEntry", "GetGoalProgressValue"])

    def test_extract_handles_function_call_step(self):
        text = "[SCOPE.sCountry('owner_country').GetAdjective]"
        chains = extract_chains(text)
        self.assertEqual(len(chains), 1)
        self.assertEqual(chains[0].steps[0], "SCOPE")
        self.assertEqual(chains[0].steps[1], "sCountry('owner_country')")
        self.assertEqual(chains[0].steps[2], "GetAdjective")

    def test_extract_skips_concept_references(self):
        chains = extract_chains("Concept ref: [concept_power_bloc] here")
        self.assertEqual(chains, [])

    def test_extract_skips_variable_substitution(self):
        chains = extract_chains("Substitution: [$DETAILS$] here")
        self.assertEqual(chains, [])

    def test_extract_keeps_concept_function_call(self):
        chains = extract_chains("[Concept('concept_x', '$fallback$')]")
        self.assertEqual(len(chains), 1)
        # Concept(...) is one step, name is "Concept"
        self.assertEqual(chains[0].steps[0].split("(")[0], "Concept")

    def test_strip_format_modifier_outside_parens(self):
        self.assertEqual(_strip_format_modifier("X.Y|D"), "X.Y")
        self.assertEqual(_strip_format_modifier("X.Y|0%"), "X.Y")
        # Bar inside parens shouldn't be treated as modifier
        self.assertEqual(_strip_format_modifier("X.sFn('a|b').Y"), "X.sFn('a|b').Y")

    def test_split_chain_respects_parens(self):
        steps = _split_chain("SCOPE.sCountry('a.b.c').GetName")
        self.assertEqual(steps, ["SCOPE", "sCountry('a.b.c')", "GetName"])


